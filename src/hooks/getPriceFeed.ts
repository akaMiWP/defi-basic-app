const dexContract = new ethers.Contract(address, dexABI, provider);

export async function getPriceFeed(
  sellTokenTicker: string,
  buyTokenTicker: string
) {
  try {
    const priceFeedInBigInt: number = await dexContract.getPriceFeedFromPair(
      tokens[sellTokenTicker],
      tokens[buyTokenTicker]
    );

    const price = ethers.utils.formatEther(priceFeedInBigInt);
    console.log(sellTokenTicker, "/", buyTokenTicker, price, priceFeedInBigInt);
    return price;
  } catch (error) {
    console.log(error);
  }
}

export const approve = async (tokenAddress: string, input: string) => {
  console.log("Waiting for approve");
  const tokenContract = new ethers.Contract(
    tokenAddress,
    erc20ABI,
    await provider.getSigner()
  );
  const amount = ethers.utils.parseEther(input);
  const txResponse = await tokenContract.approve(address, amount);
  console.log("Approve Trasanction Response:", txResponse);
};

export const swapTokens = async (
  token1: string,
  token2: string,
  input: string
) => {
  const txResponse = await dexContract.swapTokens(
    tokens[token1],
    tokens[token2],
    ethers.utils.parseEther(input)
  );
  console.log("Swap Transaction Response:", txResponse);
};
